name = input('\nWhat\'s your name?')
name = name.lower()

if name == 'alice' or name == 'mad hatter':
    print('Welcome to Wonderland!')
elif name == 'harry potter':
    print('Welcome to Hogwarts.')
else:
    print('Good to see you!')

