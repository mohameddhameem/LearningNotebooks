# Some data types in Python

print("Hello world!")   # string, str
print(100)              # integer, int
print(28.576)           # float, float
print(True)             # boolean, bool
print(False)            # boolean, bool

print(type("Hello world!"))   # string, str
print(type(100))              # integer, int
print(type(28.576))           # float, float
print(type(True))             # boolean, bool
print(type(False))            # boolean, bool

